
package com.moseeker.profile.pojo.profile;


public class Company {

    private String companyIndustry;
    private int companyProperty;
    private String companyName;
    private int companyScale;

    public void setCompanyIndustry(String companyIndustry) {
        this.companyIndustry = companyIndustry;
    }

    public String getCompanyIndustry() {
        return companyIndustry;
    }

    public void setCompanyProperty(int companyProperty) {
        this.companyProperty = companyProperty;
    }

    public int getCompanyProperty() {
        return companyProperty;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyScale(int companyScale) {
        this.companyScale = companyScale;
    }

    public int getCompanyScale() {
        return companyScale;
    }

}